//dart program to pratice number DT

void main() {
  var x = 1;
  var hex = 0xFFFF;
  var exponent = 8e5;

  print("\nX = $x");
  print("Hexa = $hex");
  print("Exponent = $exponent ");

  int i = 10;
  double d = 1.1;

  print("\nInt = $i");
  print("Double = $d");

  //DT num can be used to assign both int and double value to any variable
  num id = 1;
  num di = 1.1;

  print("\nInteger = $id");
  print("Double = $di");
}

/*
OUTPUT

X = 1
Hexa = 65535
Exponent = 800000.0

Int = 10
Double = 1.1

Integer = 1
Double = 1.1
